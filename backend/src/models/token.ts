import mongoose, { Schema, Types, model } from "mongoose";


const tokenSchema = new Schema({
    userId: {
        type: mongoose.Types.ObjectId,
        required: true,
        ref: "user",
    },
    token: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
        expires: 3600,//in secs
    }

});

export const Token = model('token', tokenSchema)